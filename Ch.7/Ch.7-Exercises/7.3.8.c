// For each of the following character escapes, give the equivalent hexadecimal
// escape.
// Assume that the character set is ASCII.

#include <stdio.h>

int main() {

    // (a): \b
    printf("(a): %x\n", '\b');
    
    // (b): \n
    printf("(b): %x\n", '\n');
    
    // (c): \r
    printf("(c): %x\n", '\r');

    // (d): \t
    printf("(d): %x\n", '\t');

    return 0;
}
